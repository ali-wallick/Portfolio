<h2>About</h2>

<p>Dead Booty is a game made for the Atari 2600 by Liz Dietsler, Harrison Leach, and myself. The game itself is a treasure hunt. The player takes control of a pirate who wants to get treasure off an island infested by zombies. He must pick up all the treasure to win. The game presented an interesting challenge in that we were extremely limited by the constraints of the systems itself.</p>
<p>To Play: Move around the screen using the arrow keys. Once off the boat, each of the four sections will have a piece of treasure. You can find it by listening to the sound that plays when near. If you are near, hit the trigger key (usually spacebar) and it will add to your score. Avoid zombies which will cause certain death. Collect 4 pieces (1000 points) to win!</p>
		
