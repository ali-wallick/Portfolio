<h2>About</h2>

<p>2015's Global Game Jam Theme was "What do we do now?" The team decided an interesting concept would be "what would happen if a game's hero were defeated, and the game had to continue along without him". From there, we decided it would be fun to have a game where the player controls the game's NPCs in an RTS-style control scheme. They must guide them through the level to complete tasks that would be easy for a hero, but is next to impossible for a regular person. Your new minions must run through fire and jump on spikes to sacrifice themselve to save "Princess" (the hero's cat) from the evil boar.</p>
<p>I participated in the game jam in 2015 with a team I had worked with several times. We decided to use this jam as a learning experience. None of us have worked with Unreal Engine 4, so we studied for the week leading up, and jumped right in to see what we could do. Through the help of many online tutorials, we were able to create a working prototype. We love our concept, and learning, so we've decided to continue to work on the project after the jam!</p>
<p>Game Over Ever After is dedicated to our professor, Tony Tseng, without whom we never would have met.</p>
		
