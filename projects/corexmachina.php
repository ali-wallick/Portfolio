<h2>About</h2>

<p><img class="imgRight" style="width:45%;" src="/images/projects/corexmachina/screenshot.jpg" />Cor Ex Machina is a game created in 48 hours for the 2013 Global Game Jam competition. It took second place for the 2013 Atlanta site (largest in the US). It is a "herding" based game designed around the theme of "heartbeat". In Cor Ex Machina, the player must herd orbs into the robot's heart as it circles around its body.</p>
<p>To play, use your mouse to move around the screen. Pressing the left mouse button will push all orbs away from you, while shrinking your character. Holding down the right mouse button causes orbs to pull toward you, while enlarging your character. You can only become so large or so small before you cannot use an ability anymore, so try to use both abilities evenly! If you are able to guide enough orbs along with you into the robot's heart, it will begin to beat again and you will win the game! If you haven't after 2 cycles around the robot, you will lose. You can see how well you are doing by examining how strong the heart is beating.</p>
<p>Cor Ex Machina was created by a team of 7 in the Unity 3D game engine. We all worked together on game design. 3 members primarily performed as coders, and 4 as artists. We also utilized the music and sound effects created specially for the game by a team of game audio students from Berklee School of Music. Check out the <a href="http://globalgamejam.org/2013/cor-ex-machina" target="_blank">Global Game Jam page for our project</a>.</p>
		
